void init_map() {
    dirt = load_texture("assets/dirt.png");
    grass = load_texture("assets/grass.png");
    water = load_texture("assets/water.png");

    load_map(level1);

    src_rect.x = src_rect.y = dst_rect.x = dst_rect.y = 0;
    src_rect.w = src_rect.h = 32;
    dst_rect.w = dst_rect.h = 32 * 2;
}

void load_map(int new_map[20][25]) {
    for (int i = 0; i < 20; i++) {
        for (int j = 0; j < 25; j++) {
            map[i][j] = new_map[i][j];
        }
    }
}

void draw_map() {
    int type = 0;
    for (int i = 0; i < 20; i++) {
        for (int j = 0; j < 25; j++) {
            type = map[i][j];

            dst_rect.x = j * dst_rect.w;
            dst_rect.y = i * dst_rect.h;

            switch (type) {
                case 0:
                    draw_texture(water, src_rect, dst_rect);
                    break;
                case 1:
                    draw_texture(grass, src_rect, dst_rect);
                    break;
                case 2:
                    draw_texture(dirt, src_rect, dst_rect);
                    break;
                default:
                    break;
            }
        }
    }
}
